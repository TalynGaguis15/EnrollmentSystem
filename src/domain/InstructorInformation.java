package domain;

public class InstructorInformation {

	Long id;
	private String firstName, middleName, lastName;

	public InstructorInformation() {

	}

	public InstructorInformation(String firstName, String middleName, String lastName) {
		this(null,firstName, middleName, lastName);
	}
	public InstructorInformation(Long id, String firstName, String middleName, String lastName) {
		this.id = id;
		this.firstName = firstName;
		this.middleName = middleName;
		this.lastName = lastName;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getMiddleName() {
		return middleName;
	}

	public void setMiddleName(String middleName) {
		this.middleName = middleName;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	
	
	

	

	
}
